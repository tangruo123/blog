def user_register(request):
    if request.method == 'POST':
        register_form = RegisterForm(request.POST)
        # 检查到数据是合法的，调用表单的 save 方法保存数据到数据库
        if register_form.is_valid():
            # 注册
            username = register_form.cleaned_data["username"]
            email = register_form.cleaned_data["email"]
            password = make_password(register_form.cleaned_data["password"])
            if User.objects.filter(username=username):
                return render(request, 'blog/register.html', context={'register_form': register_form,
                                                                      'reason': '用户名已存在'})
            user = User.objects.create(username=username, email=email, password=password, )
            user.save()
            # 登录
            user.backend = 'django.contrib.auth.backends.ModelBackend'  # 指定默认的登录验证方式
            login(request, user)
            # 返回注册之前页面
            # return redirect(request.POST.get('source_url'))

            # 重定向到首页
            return redirect("/")
        else:
            # register_form = RegisterForm()
            return render(request, 'blog/register.html', {'register_form': register_form,
                                                          'reason': register_form.errors})
    else:
        register_form = RegisterForm()
        return render(request, 'blog/register.html', locals())


